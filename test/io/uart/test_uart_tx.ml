open Hardcaml
open Hardcaml_waveterm

let clock = Signal.input "clock" 1

let waves =
  let valid = Signal.input "valid" 1 in
  let value = Signal.input "value" 32 in
  let tx, tx_busy = Mips.Uart.Tx_buffer.create ~clock { valid; value } in
  let uart_tx_value = Signal.output "uart_tx_value" tx.value in
  let uart_tx_valid = Signal.output "uart_tx_valid" tx.valid in
  let uart_tx_busy = Signal.output "uart_tx_busy" tx_busy in
  let circuit =
    Circuit.create_exn ~name:"tx_state_machine" [ uart_tx_value; uart_tx_valid; uart_tx_busy ]
  in
  let waves, sim =
    Hardcaml_waveterm.Waveform.create
      (Cyclesim.create ~config:Cyclesim.Config.trace_all circuit)
  in
  Cyclesim.cycle sim;
  Cyclesim.cycle sim;
  Cyclesim.in_port sim "valid" := Bits.vdd;
  Cyclesim.in_port sim "value" := Bits.of_string "32'h61727479" (* "arty" *);
  Cyclesim.cycle sim;
  Cyclesim.in_port sim "valid" := Bits.gnd;
  for _ = 0 to Mips.Uart.cycles_per_packet * 5 do
    Cyclesim.cycle sim
  done;
  waves

