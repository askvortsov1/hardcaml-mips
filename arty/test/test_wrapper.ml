open Hardcaml
open Hardcaml_waveterm
open Mips_arty

module Simulator = Cyclesim.With_interface (Hardcaml_arty.User_application.I) (Hardcaml_arty.User_application.O)

let testbench program n =
  let scope = Scope.create ~flatten_design:true () in
  let sim = Simulator.create (Wrapper.circuit_impl program scope) in
  let waves, sim = Waveform.create sim in

  for _i = 0 to n do
    Cyclesim.cycle sim
  done;

  waves

let display_rules =
  let re = Re.Posix.compile (Re.Posix.re "(clk|uart|led|rgb)") in
  [ Display_rule.port_name_matches re ~wave_format:Wave_format.Bit ]

let%expect_test "general integration test" =
